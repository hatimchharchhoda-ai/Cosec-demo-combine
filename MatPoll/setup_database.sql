        [TrnStat]  [numeric](1, 0)  NULL DEFAULT 0,
        [Time]     [timestamp]      NULL,
        [CreatedAt][datetime]       NULL DEFAULT GETDATE()
    ) ON [PRIMARY] TEXTIMAGE_ON [PRIMARY]
    PRINT 'Created Mat_CommTrn'
END
GO

