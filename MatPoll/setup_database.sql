-- ── Add TypeMID column to Mat_CommTrn ─────────────────────────────────────────
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID('dbo.Mat_CommTrn') AND name = 'TypeMID'
)
BEGIN
    ALTER TABLE [dbo].[Mat_CommTrn]
    ADD [TypeMID] NVARCHAR(32) NULL
    PRINT 'Added TypeMID column to Mat_CommTrn'
END
GO

-- Index for polling by TypeMID
IF NOT EXISTS (
    SELECT 1 FROM sys.indexes
    WHERE object_id = OBJECT_ID('dbo.Mat_CommTrn') AND name = 'IX_CommTrn_TypeMID'
)
BEGIN
    CREATE NONCLUSTERED INDEX IX_CommTrn_TypeMID
    ON dbo.Mat_CommTrn (TrnStat, TypeMID)
    INCLUDE (TrnID, MsgStr, RetryCnt)
    PRINT 'Index IX_CommTrn_TypeMID created'
END
GO

PRINT ''
PRINT 'TypeMID is generated by server from MAC+IP:'
PRINT '  Device 1: MAC=AA:BB:CC:DD:EE:01, IP=192.168.1.101'
PRINT '  TypeMID  = MD5("AA:BB:CC:DD:EE:01|192.168.1.101") first 12 chars'
PRINT '  This is stamped on every CommTrn row when dispatched to that device'
GO

-- ── Add DispatchedAt column to Mat_CommTrn ────────────────────────────────────
-- Stores the exact UTC time a row was sent to a device (TrnStat flipped 0→1)
-- Used to calculate ACK delay: AckReceivedAt - DispatchedAt
IF NOT EXISTS (
    SELECT 1 FROM sys.columns
    WHERE object_id = OBJECT_ID('dbo.Mat_CommTrn') AND name = 'DispatchedAt'
)
BEGIN
    ALTER TABLE [dbo].[Mat_CommTrn]
    ADD [DispatchedAt] DATETIME NULL
    PRINT 'Added DispatchedAt column to Mat_CommTrn'
END
GO

-- Index for stall recovery: find rows at TrnStat=1 with old DispatchedAt quickly
IF NOT EXISTS (
    SELECT 1 FROM sys.indexes
    WHERE object_id = OBJECT_ID('dbo.Mat_CommTrn') AND name = 'IX_CommTrn_Stall'
)
BEGIN
    CREATE NONCLUSTERED INDEX IX_CommTrn_Stall
    ON dbo.Mat_CommTrn (TrnStat, DispatchedAt)
    INCLUDE (TrnID, TypeMID, RetryCnt)
    PRINT 'Index IX_CommTrn_Stall created'
END
GO
